package ExercisesJava;
public class E13100Al200 {
    public static void main(String[] args) {
        System.out.println("Numeros pares del 100 al 200:");
        
        for (int i = 100; i <= 200; i++) {
            if (i % 2 == 0) {
                System.out.println(i);
            }
        }
    }
}
